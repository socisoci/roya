




create database Car charset=utf8;
use Car;
/* Drop Tables */

DROP TABLE CAR;
DROP TABLE MIDDLEMAN;
DROP TABLE NEWS;
DROP TABLE USER;




/* Create Tables */

CREATE TABLE CAR
(
	NAME VARCHAR(20) NOT NULL,
	YANSE VARCHAR(20),
	GONGLISHU INT,
	PINGPAI VARCHAR(20),
	PRIMARY KEY (NAME)
);


CREATE TABLE MIDDLEMAN
(
	NAME VARCHAR(20) NOT NULL,
	BIANHAO VARCHAR(30),
	SEX INT,
	GONGLING INT,
	PRIMARY KEY (NAME)
);


CREATE TABLE NEWS
(
	TIME INT NOT NULL,
	BIAMHAO INT,
	PRIMARY KEY (TIME)
);


CREATE TABLE USER
(
	USERNAME VARCHAR(20) NOT NULL,
	SEX INT,
	NIANJI YEAR(2),
	PRIMARY KEY (USERNAME)
);



